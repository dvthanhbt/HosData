    private void loadTinhThanh() throws Exception {
        lstTT = actTT.getAllTinhThanh();
        cboTinhThanh.removeAllItems();
        for (TinhThanh tt:lstTT){
            cboTinhThanh.addItem(tt.getTenTT());
        }
        cboTinhThanh.setSelectedIndex(0);
    }

    private void loadQuanHuyen() throws Exception {
        if (cboTinhThanh.getSelectedIndex() >= 0){
            lstQH = actQH.getAllQuanHuyen(lstTT.get(cboTinhThanh.getSelectedIndex()).getMaTT());
            DefaultListModel model = new DefaultListModel();
            for (QuanHuyen qh:lstQH){
                model.addElement(qh.getTenQH());
            }
            lstQuanHuyen.setModel(model);
            lstQuanHuyen.setSelectedIndex(0);
        }
    }

    private void loadXaPhuong() throws SQLException {
        String[] header = {"Mã XP", "Mã QH", "Tên Xã, Phường", "Tên tắt"};
        if (lstQuanHuyen.getSelectedIndex() >= 0){
            ResultSet rs = actXP.getXaPhuong(lstQH.get(lstQuanHuyen.getSelectedIndex()).getMaQH());
            FillData fillData = new FillData();
            fillData.fillDataJTable(jTable1, header, rs);
            rs.close();
        }
    }
