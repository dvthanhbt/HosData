    private void txtDiaChiFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDiaChiFocusLost
        try {
           if (txtDiaChi.getText().trim().length() == 0){
               maXP = "";
               return;
           }
           ResultSet rs = actDVHC.getAllDVHC(txtDiaChi.getText());
           rs.last();
           if (rs.getRow() > 0){
               if (rs.getRow() > 1){
                   JDialogSearchDVHC frm = new JDialogSearchDVHC((Frame) this.getParent(), true);
                   rs.first();
                   frm.showData(rs);
                   frm.setVisible(true);
                   rs.absolute(frm.getIndexSelected() + 1);
                   rs.next();
               }
               else {
                   rs.first();
              }
               txtDiaChi.setText(rs.getString("TenDVHC"));
               maXP = rs.getString("MaXP");
           }
           else{
               maXP = "";
               txtDiaChi.setText("");
           }
           rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }              
    }//GEN-LAST:event_txtDiaChiFocusLost

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            if (!kiemTra()) return;
            CoQuan coQuan = new CoQuan(txtCoQuanID.getText(), txtTenCoQuan.getText(), maXP, txtDienThoai.getText());
            loadData();
            actCQ.saveCoQuan(coQuan);
            loadData();
            btnNewActionPerformed(null);
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

