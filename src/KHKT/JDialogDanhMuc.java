    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtNoiDung.getText().trim().length() == 0) return;
        try {
            switch (getDanhMuc()){
                case 1:
                    Cap cap = new Cap(maSo, txtNoiDung.getText());
                    actCap.saveCap(cap);
                    break;
                case 2:
                    He he = new He(maSo, txtNoiDung.getText());
                    actHe.saveHe(he);
                    break;
                case 3:
                    LoaiNC loaiNC = new LoaiNC(maSo, txtNoiDung.getText());
                    actLoaiNC.saveLoaiNC(loaiNC);
                    break;
                case 4:
                    LoaiNV loaiNV = new LoaiNV(maSo, txtNoiDung.getText());
                    actLoaiNV.saveLoaiNV(loaiNV);
                    break;
            }
            loadData();
            maSo = "";
            txtNoiDung.setText("");
            txtNoiDung.requestFocus();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        maSo = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        txtNoiDung.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());      
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String noiDung = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 1));
        if (JOptionPane.showConfirmDialog(null, "Xoá nội dung [" + noiDung + "] phải không?", this.getTitle() , JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION){
            return;
        }
        try {
            maSo = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            switch (getDanhMuc()){
                case 1:
                    actCap.deleteCap(maSo);
                    break;
                case 2:
                    actHe.deleteHe(maSo);
                    break;
                case 3:
                    actLoaiNC.deleteLoaiNC(maSo);
                    break;
                case 4:
                    actLoaiNV.deleteLoaiNV(maSo);
                    break;
            }
            loadData();
            maSo = "";
            txtNoiDung.setText("");
            txtNoiDung.requestFocus();
        } catch (Exception e) {
            e.printStackTrace();
        }    }//GEN-LAST:event_btnDeleteActionPerformed

