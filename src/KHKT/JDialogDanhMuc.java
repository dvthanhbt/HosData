    private void showTitle() {
        switch (getDanhMuc()){
            case 1:
                this.setTitle("Danh mục cấp nghiên cứu");
                break;
            case 2:
                this.setTitle("Danh mục hệ nghiên cứu");
                break;
            case 3:
                this.setTitle("Danh mục loại nghiên cứu");
                break;
            case 4:
                this.setTitle("Danh mục loại chuyên môn");
                break;
        }
    }

    private void loadData() {
        String[] header = {"Mã số", "Nội dung"}; 
        try {
            ResultSet rs = null;
            switch (getDanhMuc()){
                case 1:
                    rs = actCap.getListCap();
                    break;
                case 2:
                    rs = actHe.getListHe();
                    break;
                case 3:
                    rs = actLoaiNC.getListLoaiNC();
                    break;
                case 4:
                    rs = actLoaiNV.getListLoaiNV();
                    break;
            }
            FillData fillData = new FillData();
            fillData.fillDataJTable(jTable1, header, rs);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
