    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        XaPhuong xaPhuong = new XaPhuong(txtMaXP.getText(),
            lstQH.get(lstQuanHuyen.getSelectedIndex()).getMaQH(),
            txtTenXP.getText(), txtTenTat.getText().toUpperCase());
        try {
            actXP.saveXaPhuong(xaPhuong);
            loadXaPhuong();
            btnNewActionPerformed(null);
            btnEdit.setText("Sửa");
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (jTable1.getSelectedRow() >= 0){
            txtMaXP.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            txtTenXP.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            txtTenTat.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
            txtMaXP.setEnabled(false);
            txtTenXP.requestFocus();
        }
    }//GEN-LAST:event_btnEditActionPerformed

