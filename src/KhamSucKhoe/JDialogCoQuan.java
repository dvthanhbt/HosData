    private void loadData() throws Exception {
        String[] header = {"Mã CQ", "Tên cơ quan, doanh nghiệp", "Địa chỉ", "Điện thoại"};
        ResultSet rs = actCQ.getListCoQuan();
        FillData fillData = new FillData();
        fillData.fillDataJTable(jTable1, header, rs);
    }

    private boolean kiemTra() {
        if (txtTenCoQuan.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập tên cơ quan, doanh nghiệp");
            txtTenCoQuan.requestFocus();
            return false;
        }
        if (txtDiaChi.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ cơ quan, doanh nghiệp");
            txtDiaChi.requestFocus();
            return false;
        }
        return true;
    }
