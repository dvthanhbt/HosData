    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        QuocGia quocGia = new QuocGia(txtQuocGiaID.getText(),
                txtTenQuocGia.getText(),
                txtEnglish.getText(), 
                txtKyHieu.getText().toUpperCase());
        try {
            actQG.saveQuocGia(quocGia);
            loadQuocGia();
            btnNew2ActionPerformed(null);
            btnEdit.setText("Sửa");
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (btnEdit.getText() == "Sửa"){
            if (jTable1.getSelectedRow() >= 0){
                txtQuocGiaID.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                txtTenQuocGia.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
                txtEnglish.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
                txtKyHieu.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
                txtQuocGiaID.setEnabled(false);
                txtTenQuocGia.requestFocus();
            }
            btnEdit.setText("Huỷ");
        } else {
            btnNew2ActionPerformed(null);
            btnEdit.setText("Sửa");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            actQG.deleteQuocGia(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            loadQuocGia();
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

