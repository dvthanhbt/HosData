    private void loadQuocGia() throws SQLException {
        String[] header = {"Mã QG", "Tên Quốc gia", "English", "Ký hiệu"};
        ResultSet rs = actQG.getQuocGia();
        FillData fillData = new FillData();
        fillData.fillDataJTable(jTable1, header, rs);
        rs.close();
    }
